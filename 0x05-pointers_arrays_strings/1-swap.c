#include "main.h"
/**
 * swap_int - exchanges (swaps) the values of any two integers.
 *
 * @a: first input integer argument for alteration
 * @b: second input integer argument for alteration
 *
 * Return: void (nothing)
 */
void swap_int(int *a, int *b)
{
	/**
	 * To swap, do these steps:
	 *
	 * 1. *a = b;
	 * 2. *b = a
	 *
	 */

	*a = 42;

	*b = 98;
}
